package JavaPractice01_Method_Condition;

public class StringInterpolation2 {
    public static void main(String[] args) {
        // 변수 생성
        double x = 7.0 / 2.0; // 3.5
        double y = 7 / 2; // 3 => (자동 변환) => 3.0

        // 결과 출력
        //printf %f = 실수형
        System.out.printf("x = %f, y = %f", x, y);
    }
}
