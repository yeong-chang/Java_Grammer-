package JavaPractice01_Method_Condition;

public class StringInterpolation {
    public static void main(String[] args) {
        // 변수 생성
        int result = 7 / 2; // 정수를 정수로 연산한 결과는 정수

        // 결과 출력
        //printf %d = 정수형
        System.out.printf(
                "7 / 2는 %d입니다.", result);
    }
}
